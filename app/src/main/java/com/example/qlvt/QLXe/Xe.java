package com.example.qlvt.QLXe;

public class Xe {
    String maXe, tenXe, namSX, maTX;
    byte [] imgXe;
    public Xe() {
        this.maXe = maXe;
        this.tenXe = tenXe;
        this.namSX = namSX;
        this.maTX = maTX;
        this.imgXe = imgXe;
    }

    public String getMaXe() {
        return maXe;
    }

    public void setMaXe(String maXe) {
        this.maXe = maXe;
    }

    public String getTenXe() {
        return tenXe;
    }

    public void setTenXe(String tenXe) {
        this.tenXe = tenXe;
    }

    public String getNamSX() {
        return namSX;
    }

    public void setNamSX(String namSX) {
        this.namSX = namSX;
    }

    public String getMaTX() {
        return maTX;
    }

    public void setMaTX(String maTX) {
        this.maTX = maTX;
    }

    public byte[] getImgXe() {
        return imgXe;
    }

    public void setImgXe(byte[] imgXe) {
        this.imgXe = imgXe;
    }
}

