package com.example.qlvt.THONGKE;

public class ThongKeTuyen {
    String maTuyen, soLan;
    public ThongKeTuyen() {
        this.maTuyen = maTuyen;
        this.soLan = soLan;
    }

    public String getMaTuyen() {
        return maTuyen;
    }

    public void setMaTuyen(String maTuyen) {
        this.maTuyen = maTuyen;
    }

    public String getSoLan() {
        return soLan;
    }

    public void setSoLan(String soLan) {
        this.soLan = soLan;
    }
}

